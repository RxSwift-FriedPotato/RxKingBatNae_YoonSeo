import UIKit

extension UIColor{
    class var KakaoYellow: UIColor  {return UIColor(red: 255.0 / 255.0, green: 234.0 / 255.0, blue: 42.0 / 255.0, alpha: 1.0)}
    
    class var KakaoLightGray: UIColor {return UIColor(red: 246.0 / 255.0, green: 246.0 / 255.0, blue: 246.0 / 255.0, alpha: 1.0)}
    
    class var KakaoGray: UIColor {return UIColor(red: 212.0 / 255.0, green: 212.0 / 255.0, blue: 212.0 / 255.0, alpha: 1.0)}
    
    class var  KakaoDarkGray: UIColor {return UIColor(red: 180.0 / 255.0, green: 180.0 / 255.0, blue: 180.0 / 255.0, alpha: 1.0)}
}
